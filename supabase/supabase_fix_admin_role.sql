-- ─────────────────────────────────────────────────────────────
-- 1. عيّن role = 'admin' لأي مستخدم بإيميله
--    *** غير 'your-admin@email.com' بالإيميل الصحيح ***
-- ─────────────────────────────────────────────────────────────
UPDATE public.profiles
SET role = 'admin', updated_at = NOW()
WHERE id = (
  SELECT id FROM auth.users
  WHERE email = 'swagolive91@gmail.com'   -- ← غيّر هنا
  LIMIT 1
);

-- ─────────────────────────────────────────────────────────────
-- 2. تحقق من النتيجة
-- ─────────────────────────────────────────────────────────────
SELECT p.id, u.email, p.role, p.admin_role, p.updated_at
FROM public.profiles p
JOIN auth.users u ON u.id = p.id
WHERE p.role = 'admin'
ORDER BY p.updated_at DESC;

